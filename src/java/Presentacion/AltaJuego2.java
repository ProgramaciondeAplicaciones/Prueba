    private void boton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton1ActionPerformed

        if(lista1.getSelectedIndex()==-1){
            JOptionPane.showMessageDialog(null, "No seleciono ninguna categoria");
        }else{
            String elem=lista1.getSelectedValue().toString();
            int indice = lista1.getSelectedIndex();
            
            modell2.addElement(elem);
            lista2.setModel(modell2);
            modell.remove(indice);
            lista1.setModel(modell);
            listaa.add(elem);
           cont1.categoriasparaeljuego.add(elem);
        }

    }//GEN-LAST:event_boton1ActionPerformed

