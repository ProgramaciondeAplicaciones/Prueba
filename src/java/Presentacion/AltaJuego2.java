 c_desarrolladores.getEditor().getEditorComponent().addKeyListener(new KeyAdapter(){
       
           @Override
         public void  keyReleased(KeyEvent evt){
           
         String cadenaEscrita=c_desarrolladores.getEditor().getItem().toString();
         //limitar los eventos de las teclas usando el ACII 
         if(evt.getKeyCode()>=65 && evt.getKeyCode()<=90 || evt.getKeyCode()>=96 && evt.getKeyCode()<=105 || evt.getKeyCode()==8 ){
             try {            
                 c_desarrolladores.setModel(busqueda.getListaDesarrolladores(cadenaEscrita));
                 if(c_desarrolladores.getItemCount()>0){
                     c_desarrolladores.showPopup();
                     if(evt.getKeyCode()!=8){
                         ((JTextComponent)c_desarrolladores.getEditor().getEditorComponent()).select(cadenaEscrita.length(),c_desarrolladores.getEditor().getItem().toString().length());
                         
                     }
                     else{
                         c_desarrolladores.getEditor().setItem(cadenaEscrita);
                     }
                 }
                 else{
                     c_desarrolladores.addItem(cadenaEscrita);
                 }
             } catch (SQLException ex) {
                 Logger.getLogger(ConsultaPerfil.class.getName()).log(Level.SEVERE, null, ex);
             }
             
             
             
         }
             
             
       }
         
         
           
       
       });
