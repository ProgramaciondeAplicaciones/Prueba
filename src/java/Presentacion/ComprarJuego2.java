    public DataFecha obtenerFecha(){
            String ddd = (String) t_dia1.getText();
            String mmm = (String) t_mes1.getText();
            String aaa = (String) t_anio1.getText();
            int dd=Integer.parseInt(ddd);
            int aa=Integer.parseInt(aaa);
            int mm=Integer.parseInt(mmm);
            DataFecha fecha = new DataFecha(dd,mm,aa);
            return fecha;
           
}
    
    public boolean mostrarFechaCorrecta(){
        
    Calendar fechaActual = Calendar.getInstance();
 
    int anios = fechaActual.get(Calendar.YEAR);
    int meses = fechaActual.get(Calendar.MONTH);
    int dias = fechaActual.get(Calendar.DAY_OF_MONTH);
        
    String ddd = (String) t_dia1.getText();    
    String mmm = (String) t_mes1.getText();      
    String aaa = (String) t_anio1.getText();   
    int dd=Integer.parseInt(ddd);       
    int aa=Integer.parseInt(aaa);
    int mm=Integer.parseInt(mmm);
        
    
    if(aa <= anios && aa>= 1900){
        if(mm == 1 || mm == 3 || mm == 5 || mm == 7 || mm == 8 || mm == 10 || mm == 12){
            if((dd >= 1 && dd <= 31) && aa != anios){
                return true;
            }
            else{
                if(aa == anios && mm <= meses && dd < dias){
                    return true;
                }
                else{
                    if(aa == anios && mm < meses){
                        return true;
                    }
                    else{
                        return false;
                    }
                }
            }
        }
        else{
            if(mm == 4 || mm == 6 || mm == 9 || mm == 11){
                if((dd >= 1 && dd <= 30) && aa != anios){
                    return true;
                }
                else{
                    if(aa == anios && mm == meses && dd < dias){
                        return true;
                    }
                    else{
                        if(aa == anios && mm < meses){
                            return true;
                        }
                        else{
                            return false;
                        }
                    }
                }
            }
            else{
                if(mm == 2){
                    if((dd >= 1 && dd <= 29) && aa != anios){
                        return true;
                    }
                    else{
                        if(aa == anios && mm == meses && dd <= dias){
                            return true;
                        }
                        else{
                            if(aa == anios && mm < meses){
                                return true;
                            }
                            else{
                                return false;
                            }
                        }
                    }
                }
                else{
                    return false;
                }
            }
        }
    }
    else{
        return false;
    }  
    }
